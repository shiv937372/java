public class patternnumber
 {
    public static void main(String args[])
    {
         int i,j,count=1 ,n=6;
     for(i=0;i<n;i++)
     {
       
        for(j=0;j<i;j++)
        {
            System.out.print(" " +count);
            count++;
        }
       
         System.out.println();
    }
}
}
